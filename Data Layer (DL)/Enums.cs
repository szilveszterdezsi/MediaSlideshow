namespace DL
{
    /// <summary>
    /// Media types.
    /// </summary>
    public enum MediaType
    {
        Image, Video, Audio
    }

    /// <summary>
    /// Image extension types.
    /// </summary>
    public enum ImageExtension
    {
        jpg,
        jpeg,
        tif,
        tiff,
        png,
        gif,
        bmp,
    }

    /// <summary>
    /// Video extension types.
    /// </summary>
    public enum VideoExtension
    {
        mov,
        mp4,
        wmv,
        avi,
    }

    /// <summary>
    /// Audio extension types.
    /// </summary>
    public enum AudioExtension
    {
        wav,
        mp3,
        wma,
    }
}
